          <p><span class="fntbold">A.</span> PSL teams will be drafted on the snake draft bidding methodology in a live event attended by team owners and mentors. The snake draft is divided into rounds— one round for every available roster spot on each team. For example, if the league has 8-man rosters, your draft will have 8 rounds. In a snake draft, each Team owner has one pick in each round. Each team makes its first-round pick based on a predetermined order. When the first round is over, the team that picked last in the first round picks first in the second round — in other words, the draft snakes back on itself. The team that had the first pick in the first round now has the last pick in the second round and the first pick in the third round. This goes on till all the teams are drafted.</p>
